#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*new;

	new = (t_list *)malloc(sizeof(t_list) * 1);
	new->content = content;
	//printf("%s\n", (char *)content);
	new->next = NULL;
	return (new);
}
